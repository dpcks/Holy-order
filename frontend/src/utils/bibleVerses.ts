export const getDailyVerse = (): BibleVerse => {
  const MS_PER_DAY = 1000 * 60 * 60 * 24;
  const daysSinceEpoch = Math.floor(Date.now() / MS_PER_DAY);
  const verseIndex = daysSinceEpoch % VOLUNTEER_VERSES.length;
  return VOLUNTEER_VERSES[verseIndex];
};
